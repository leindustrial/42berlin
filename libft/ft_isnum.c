#include "libft.h"

int	ft_isnum(char *av)
{
	int	i;

	i = 0;
	while (av[i] != '\0')
	{
		if (ft_issign(av[i]) == 1)
			i++;
		if (ft_isdigit(av[i]) == 0)
			return (0);
		i++;
	}
	return (1);
}
