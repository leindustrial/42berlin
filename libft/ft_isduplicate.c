int	ft_isduplicate(int ac, int *arr)
{
	int	i;
	int	j;

	i = 0;
	while (i < (ac - 1))
	{
		j = i + 1;
		while (j <= (ac - 1))
		{
			if (arr[i] == arr[j])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
