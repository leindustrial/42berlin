#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	size_t	slen;
	char	*dst;

	slen = ft_strlen(s);
	dst = (char *)malloc(sizeof(char) * (slen + 1));
	if (dst == 0)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
