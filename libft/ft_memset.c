#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*ch;
	size_t			i;

	i = 0;
	ch = (unsigned char *)s;
	while (i < n)
	{
		ch[i] = c;
		i++;
	}
	return (s);
}
